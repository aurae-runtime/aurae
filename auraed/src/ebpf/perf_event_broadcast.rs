use tokio::sync::broadcast::{Receiver, Sender};

#[derive(Debug, Clone)]
pub struct PerfEventBroadcast<T>(Sender<T>);

impl<T> PerfEventBroadcast<T> {
    pub fn new(tx: Sender<T>) -> Self {
        Self(tx)
    }

    pub fn subscribe(&self) -> Receiver<T> {
        self.0.subscribe()
    }
}